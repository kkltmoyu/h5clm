/**
 * [转换对象key属性=name属性]
 * @param  {Array}  data  [数据集合]
 * @param  {[Array]} names [名称集合]
 * @return {[Array]}       [转换之后的结果]
 * [{"key":"high","value":30},{"key":"middle","value":45},{"key":"low","value":25}]
 * =>[{"name":"high","value":30},{"name":"middle","value":45},{"name":"low","value":25}]
 */
chartUtil.keyConvertName = (data = [], names, fixed = 2) => {
    return data.map((item, index) => {
        const name = Object.prototype.toString.call(names) === '[object Object]' ? names[item.key] : Object.prototype.toString.call(names) === '[object Array]' ? names[index] : item.key;
        return Object.assign({}, item, {
            name: name,
            value: item.value ? Number((item.value).toFixed(fixed)) : 0
        });
    });
}
/**
 * [计算tooltip的位置]
 * @param  {[type]} point  [鼠标位置]
 * @param  {[type]} params [同 formatter 的参数相同]
 * @param  {[type]} dom    [tooltip 的 dom 对象]
 * @param  {[type]} rect   [只有鼠标在图形上时有效，是一个用x, y, width, height四个属性表达的图形包围盒]
 * @param  {[type]} size   [包括 dom 的尺寸和 echarts 容器的当前尺寸，例如：{contentSize: [width, height], viewSize: [width, height]}]
 * @return {[type]}        [[x,y]]
 */
chartUtil.setPosition = (point, params, dom, rect, size) => {
    let _point = [...point];
    const contentSize = size.contentSize; //弹出层的尺寸[w,h]
    const viewSize = size.viewSize; //echarts容器的尺寸
    const x = point[0],
        y = point[1]; //鼠标
    _point[0] = x + contentSize[0] > viewSize[0] ? x - contentSize[0] - 10 : _point[0] + 10;
    _point[1] = y + contentSize[1] > viewSize[1] ? y - contentSize[1] - 5 : _point[1] + 5;
    return _point;
}
/**
 * [计算TGI]
 * @param  {[type]} data [description]
 * @return {[type]}      [description]
 */
chartUtil.calculateTGI = (data) => {
    let tgiData = data.map((item) => {
        let newItem = chartUtil.mergeRecursive({}, item);
        if (item.hasOwnProperty('city')) {
            //https://jira.tendcloud.com/browse/SLBS-410
            //洞察和对比中的数据未对应上

            //做四舍五入
            newItem.cityTGI = typeof item.city === 'number' && item.city !== 0 ? Math.round((typeof item.value === 'number' ? item.value : 0) / item.city * 100) : 0;
            //未做四舍五入用于比较大小
            newItem.cityTGI_uncut = typeof item.city === 'number' && item.city !== 0 ? ((typeof item.value === 'number' ? item.value : 0) / item.city * 100) : 0;
        }
        if (item.hasOwnProperty('area')) {
            //做四舍五入
            newItem.areaTGI = typeof item.city === 'number' && item.area !== 0 ? Math.round((typeof item.value === 'number' ? item.value : 0) / item.area * 100) : 0;
            //未做四舍五入用于比较大小
            newItem.areaTGI_uncut = typeof item.city === 'number' && item.area !== 0 ? ((typeof item.value === 'number' ? item.value : 0) / item.area * 100) : 0;
        }
        return newItem;

    });
    return tgiData;

}
